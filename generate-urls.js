// Function to create Amazon URL from ISBN or ASIN
function createAmazonUrl(title, isbn) {
  // Check if we have a known ASIN
  const normalizedTitle = title.replace(/['"]/g, '').trim();
  
  // Try to find a match in knownASINs
  for (const [bookTitle, asin] of Object.entries(knownASINs)) {
    if (normalizedTitle.toLowerCase().includes(bookTitle.toLowerCase()) || 
        bookTitle.toLowerCase().includes(normalizedTitle.toLowerCase())) {
      return `https://www.amazon.com/dp/${asin}?tag=${affiliateId}`;
    }
  }

  // If no known ASIN, try ISBN-10 or ISBN-13
  if (isbn) {
    const cleanIsbn = isbn.replace(/-/g, '');
    if (cleanIsbn.length === 10) {
      return `https://www.amazon.com/dp/${cleanIsbn}?tag=${affiliateId}`;
    } else if (cleanIsbn.length === 13) {
      // Convert ISBN-13 to ISBN-10 if possible, or use 13
      return `https://www.amazon.com/dp/${cleanIsbn}?tag=${affiliateId}`;
    }
  }

  // Fallback: search URL (less ideal)
  const searchTerm = encodeURIComponent(normalizedTitle);
  return `https://www.amazon.com/s?k=${searchTerm}&tag=${affiliateId}`;
}

